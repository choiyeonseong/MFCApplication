	// DLL이 익스포트하는 함수를 정의한다.
	__declspec(dllexport) BOOL ShowSetupDialog(char* szMail, char* szPath)
	{
		CSetupDlg dlg;
		dlg.m_Path = szPath;	// 설치 디렉토리 경로 설정
		if (dlg.DoModal() == IDOK)
		{
			lstrcpy((LPWSTR)szMail, dlg.m_Mail);	// 사용자로부터 입력받은 값 복사
			lstrcpy((LPWSTR)szPath, dlg.m_Path);	// 사용자로부터 입력받은 값 복사
			return TRUE;
		}
		return FALSE;
	 }
